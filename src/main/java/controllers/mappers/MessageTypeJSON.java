package controllers.mappers;

/**
 * Mapper for type JSON
 */
public class MessageTypeJSON {

    private String name;

    private String iconName;

    private String iconValue;

    public String getName() {
        return name;
    }

    public String getIconName() {
        return iconName;
    }

    public String getIconValue() {
        return iconValue;
    }
}
