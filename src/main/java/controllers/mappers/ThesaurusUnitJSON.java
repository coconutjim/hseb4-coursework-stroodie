package controllers.mappers;

/**
 * Mapper for thesaurus JSON
 */
public class ThesaurusUnitJSON {

    private String mode;

    private int id;

    private String name;

    private String content;

    private String text;

    public String getMode() {
        return mode;
    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getContent() {
        return content;
    }

    public String getText() {
        return text;
    }
}
